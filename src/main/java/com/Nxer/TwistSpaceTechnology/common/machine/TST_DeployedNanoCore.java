    public static IStructureDefinition<TST_DeployedNanoCore> STRUCTURE_DEFINITION = null;

    @Override
    public void construct(ItemStack stackSize, boolean hintsOnly) {
        buildPiece(
            STRUCTURE_PIECE_BOTTOM,
            stackSize,
            hintsOnly,
            horizontalOffSetBottom,
            verticalOffSetBottom,
            depthOffSetBottom);
        buildPiece(
            STRUCTURE_PIECE_MIDDLE,
            stackSize,
            hintsOnly,
            horizontalOffSetMiddle,
            verticalOffSetMiddle,
            depthOffSetMiddle);
    }

    @Override
    public int survivalConstruct(ItemStack stackSize, int elementBudget, ISurvivalBuildEnvironment env) {
        if (mMachine) return -1;

        int builtBottom = survivialBuildPiece(
            STRUCTURE_PIECE_BOTTOM,
            stackSize,
            horizontalOffSetBottom,
            verticalOffSetBottom,
            depthOffSetBottom,
            elementBudget,
            env,
            false,
            false);

        int builtMiddle = survivialBuildPiece(
            STRUCTURE_PIECE_MIDDLE,
            stackSize,
            horizontalOffSetMiddle,
            verticalOffSetMiddle,
            depthOffSetMiddle,
            elementBudget,
            env,
            false,
            false);

        // building finished
        if (builtBottom == -1 && builtMiddle == -1) return -1;

        // only Bottom finished
        if (builtBottom == -1 && builtMiddle >= 0) return builtMiddle;

        // only Middle finished
        if (builtMiddle == -1 && builtBottom >= 0) return builtBottom;

        // building
        return builtBottom + builtMiddle;
    }

    @Override
    public IStructureDefinition<TST_DeployedNanoCore> getStructureDefinition() {
        if (STRUCTURE_DEFINITION == null) {
            STRUCTURE_DEFINITION = StructureDefinition.<TST_DeployedNanoCore>builder()
                .addShape(STRUCTURE_PIECE_BOTTOM, transpose(SHAPE_BOTTOM))
                .addShape(STRUCTURE_PIECE_MIDDLE, transpose(SHAPE_MIDDLE))
                .addElement('A', ofBlock(GregTech_API.sBlockCasings2, 5))
                .addElement('B', ofBlock(GregTech_API.sBlockCasings2, 8))
                .addElement('C', ofBlock(GregTech_API.sBlockCasings8, 10))
                .addElement('D', ofBlock(sBlockCasingsTT, 9))
                .addElement('E', ofBlock(sBlockCasingsTT, 10))
                .addElement('F', ofBlock(sBlockCasingsTT, 12))
                .addElement(
                    'G',
                    GT_HatchElementBuilder.<TST_DeployedNanoCore>builder()
                        .atLeast(InputBus, OutputBus, InputHatch, OutputHatch, Energy.or(ExoticEnergy))
                        .adder(TST_DeployedNanoCore::addToMachineList)
                        .dot(1)
                        .casingIndex(((GT_Block_Casings8) GregTech_API.sBlockCasings8).getTextureIndex(10))
                        .buildAndChain(GregTech_API.sBlockCasings8, 10))
                .build();
        }
        return STRUCTURE_DEFINITION;
    }

